#include <unistd.h>

void	ft_putstr(char *str)
{
	int i;

	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
}

void	ft_generic(void)
{
	ft_putstr("Tut tut ; Tut tut\n");
}
